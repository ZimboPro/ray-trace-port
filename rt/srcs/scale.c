#include "rt.h"

/*
 ** scale
*/

float		**scale(float sx, float sy, float sz)
{
	float	**sc;

	sc = ft_ftarrnew(4, 4);
	sc[0][0] = sx;
	sc[0][1] = 0;
	sc[0][2] = 0;
	sc[0][3] = 0;
	sc[1][0] = 0;
	sc[1][1] = sy;
	sc[1][2] = 0;
	sc[1][3] = 0;
	sc[2][0] = 0;
	sc[2][1] = 0;
	sc[2][2] = sz;
	sc[2][3] = 0;
	sc[3][0] = 0;
	sc[3][1] = 0;
	sc[3][2] = 0;
	sc[3][3] = 1;
	return (sc);
}
