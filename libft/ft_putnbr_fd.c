#include "libft.h"

void			ft_putnbr_fd(int n, int fd)
{
	int		x;
	char	c;

	x = n;
	if (x == -2147483648)
		write(fd, "-2147483648", 11);
	else
	{
		if (x < 0)
		{
			c = '-';
			x = -x;
			write(fd, &c, 1);
		}
		if (x >= 10)
		{
			ft_putnbr_fd(x / 10, fd);
			ft_putnbr_fd(x % 10, fd);
		}
		else
		{
			c = x + 48;
			write(fd, &c, 1);
		}
	}
}
