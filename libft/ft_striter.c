#include "libft.h"

void	ft_striter(char *str, void (*f)(char *))
{
	int	i;

	if (str != NULL && f != NULL)
	{
		i = 0;
		while (str[i] != '\0')
		{
			f(&str[i]);
			i++;
		}
	}
}
