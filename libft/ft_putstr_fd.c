#include "libft.h"

void	ft_putstr_fd(char const *str, int fd)
{
	int	i;

	if (str != NULL)
	{
		i = 0;
		while (str[i] != '\0')
		{
			write(fd, &str[i], 1);
			i++;
		}
	}
}
