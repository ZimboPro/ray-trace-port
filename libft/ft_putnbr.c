#include "libft.h"

void		ft_putnbr(int n)
{
	int		x;
	char	c;

	x = n;
	if (x == -2147483648)
		write(1, "-2147483648", 11);
	else
	{
		if (x < 0)
		{
			c = '-';
			x = -x;
			write(1, &c, 1);
		}
		if (x >= 10)
		{
			ft_putnbr(x / 10);
			ft_putnbr(x % 10);
		}
		else
		{
			c = x + 48;
			write(1, &c, 1);
		}
	}
}
