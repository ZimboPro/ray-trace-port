static float	lin_inter(float x, float y, float s)
{
	return (x + s * (y - x));
}

static float	smooth_inter(float x, float y, float s)
{
	return (lin_inter(x, y, s * s * (3 - 2 * s)));
}

static float	noise2d(float x, float y)
{
	t_noise		n;
	static int	seed = 179;

	n.x_int = x;
	n.y_int = y;
	n.x_frac = x - n.x_int;
	n.y_frac = y - n.y_int;
	n.s = noise2(n.x_int, n.y_int, seed);
	n.t = noise2(n.x_int + 1, n.y_int, seed);
	n.u = noise2(n.x_int, n.y_int + 1, seed);
	n.v = noise2(n.x_int + 1, n.y_int + 1, seed);
	n.low = smooth_inter(n.s, n.t, n.x_frac);
	n.high = smooth_inter(n.u, n.v, n.x_frac);
	return (smooth_inter(n.low, n.high, n.y_frac));
}

float			perlin2d(float x, float y, float freq, int depth)
{
	t_noise	n;
	int		i;

	n.xa = x * freq;
	n.ya = y * freq;
	n.amp = 1.0;
	n.fin = 0;
	n.div = 0.0;
	i = 0;
	while (i < depth)
	{
		n.div += 256 * n.amp;
		n.fin += noise2d(n.xa, n.ya) * n.amp;
		n.amp /= 2;
		n.xa *= 2;
		n.ya *= 2;
		i++;
	}
	return (n.fin / n.div);
}
